  @test prime(400) ≡ 2741

  @test prime(700) ≡ 5279

  @test prime(1049) ≡ 8377

  @test prime(10001) ≡ 104743

  @test_throws ArgumentError prime(100_001)

  @test prime(20_001; lim=1_000_000) ≡ 224_743

